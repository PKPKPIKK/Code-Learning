package com.LT.Learning.LV1.loopfor;

public class forDemo2 {
    public static void main(String[] args) {
        // 练习1
        for(int i = 1;i <= 5; i++){
            System.out.println("数字" + i);
        }
        for(int i = 5;i >= 1; i--){
            System.out.println("数字" + i);
        }
        }
    }

