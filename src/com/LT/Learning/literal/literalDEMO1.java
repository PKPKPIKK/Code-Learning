package com.LT.Learning.literal;

public class literalDEMO1 {
    public static void main(String[] args) {
        // 输出恐龙的信息：霸王龙；8岁；11.5，公

        //输出恐龙的名字霸王龙
        System.out.println("霸王龙");
        //输出恐龙的年龄8岁
        System.out.println(8);
        //输出恐龙的体重11.5
        System.out.println(11.5);
        //输出恐龙的性别公
        System.out.println("公");

    }
}
